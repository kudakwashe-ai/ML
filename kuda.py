"""# Part 2: SMS Spam Detection Deployment
In this section, we build the SMS Spam classifier, save the model components, and prepare the deployment files.
"""

import pandas as pd
import pickle
import requests
import io
from sklearn.feature_extraction.text import CountVectorizer
from sklearn.naive_bayes import MultinomialNB

# Step 1: Download and Load Dataset
url = "https://raw.githubusercontent.com/justmarkham/pycon-2016-tutorial/master/data/sms.tsv"
response = requests.get(url)

# Save the raw file as SMSSpamCollection
with open('SMSSpamCollection', 'w') as f:
    f.write(response.text)

# Load into DataFrame
df = pd.read_table(io.StringIO(response.text), header=None, names=['label', 'message'])

# Step 2: Training
X = df['message']
y = df['label']
vectorizer = CountVectorizer()
X_vec = vectorizer.fit_transform(X)
model = MultinomialNB()
model.fit(X_vec, y)

# Step 3: Save required deployment elements
with open('model.pkl', 'wb') as f:
    pickle.dump(model, f)
with open('vectorizer.pkl', 'wb') as f:
    pickle.dump(vectorizer, f)

print("Files created: SMSSpamCollection, model.pkl, vectorizer.pkl")

