"""# Part 2: SMS Spam Detection Deployment
In this section, we build the SMS Spam classifier using Naive Bayes, save the model and vectorizer, and prepare the deployment files.
"""

import pandas as pd
import pickle
import requests
import io
from sklearn.feature_extraction.text import CountVectorizer
from sklearn.naive_bayes import MultinomialNB

# Step 1: Download and Load Dataset
url = "https://raw.githubusercontent.com/justmarkham/pycon-2016-tutorial/master/data/sms.tsv"
response = requests.get(url)

# Load into DataFrame
df = pd.read_table(io.StringIO(response.text), header=None, names=['label', 'message'])

# Save the raw file as requested in Step 3 of the guide
with open('SMSSpamCollection', 'w') as f:
    f.write(response.text)

# Step 2: Preprocessing and Training
X = df['message']
y = df['label']

vectorizer = CountVectorizer()
X_vec = vectorizer.fit_transform(X)

model = MultinomialNB()
model.fit(X_vec, y)

# Step 3: Save required deployment elements
with open('model.pkl', 'wb') as f:
    pickle.dump(model, f)

with open('vectorizer.pkl', 'wb') as f:
    pickle.dump(vectorizer, f)

print("Part 2 files created: SMSSpamCollection, model.pkl, vectorizer.pkl")

"""## Create the Streamlit App for Spam Detection (`spam.py`)"""

spam_app_code = """
import streamlit as st
import pickle

# Load model and vectorizer
model = pickle.load(open('model.pkl', 'rb'))
vectorizer = pickle.load(open('vectorizer.pkl', 'rb'))

st.title('SMS Spam Detection App')
st.write('Enter an SMS message below to check if it is Spam or Ham.')

user_input = st.text_area("Enter SMS Message Here:")

if st.button('Detect'):
    if user_input.strip() != '':
        data = [user_input]
        vect = vectorizer.transform(data).toarray()
        prediction = model.predict(vect)
        result = prediction[0]

        if result == 'spam':
            st.error(f'Result: This message is {result.upper()}')
        else:
            st.success(f'Result: This message is {result.upper()}')
    else:
        st.warning("Please enter some text.")
"""

with open('spam.py', 'w') as f:
    f.write(spam_app_code)

print("spam.py created.")

